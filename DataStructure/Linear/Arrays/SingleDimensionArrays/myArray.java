package DataStructure.Linear.Arrays.SingleDimensionArrays;

public class myArray {
   public static void main(String[] args) {
      double[] marks = new double[3];
      marks[0] = 89;
      marks[1] = 90;
      marks[2] = 91;
      for (double mark : marks) {
         System.out.println("Mark:" + mark);
      }
   }
}
